document.getElementById('random').addEventListener('mouseover',function() {
    var color = '#'+Math.floor(Math.random()*16777215).toString(16);
    var colorString = color;
    document.getElementById('random').style.backgroundColor = colorString;
    document.getElementById('random').style.webkitBackgroundColor = colorString;
    document.getElementById('random').style.moxBackgroundColor = colorString;
});

